var closeSpan3=document.querySelector("#close3")
var addSpan3=document.querySelector("#add3")
document.querySelector("#btn3").addEventListener("click", function() {
        var p3=document.querySelector("#p3")
        p3.style.display=(p3.style.display==="none")?"block":"none"
        closeSpan3.style.display=(closeSpan3.style.display==="none")?"block":"none"
        addSpan3.style.display=(addSpan3.style.display==="block")?"none":"block"
    
})
